package day03;

public class Ascii {
	public static void main(String[]args) {
		
		for(char i='A'; i <='Z'; i++ ) {
			int s =  i;
			System.out.print(i+" " +s +"  ");
		}
		System.out.println("");
		for(char i='a'; i <='z'; i++ ) {
			int s =  i;
			System.out.print(i+" " +s +"  ");
		}
	}
}
